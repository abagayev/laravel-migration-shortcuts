<?php

namespace Abagayev\Laravel\MigrationShortcuts\Migrations;

use Illuminate\Database\Migrations\Migration;

abstract class EmptyMigration extends Migration
{
    /**
     * Run the migrations.
     *
     * @return void
     */
    public function up()
    {
        // i'm a little pea
        // i love the sky and the trees
    }

    /**
     * Reverse the migrations.
     *
     * @return void
     */
    public function down()
    {
        // i'm a teeny, tiny little ant
        // checking out this and that
    }
}
